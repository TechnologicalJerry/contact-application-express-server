const mongoose = require('mongoose');

const schema = mongoose.Schema({
    firstName: {
        type: String,
        require: true
    },
    lastName: {
        type: String,
        require: true
    },
    email: {
        type: String,
        require: true
    },
    phone: {
        type: Number,
        require: true
    }
});

module.exports = mongoose.model('Contact', schema);